                        y = i;
                        Console.Write("^");
                    }
                    else if (map[i, j] == 1) Console.Write("#");
                    else Console.Write(" ");
                }
                Console.WriteLine();
            }
        }

        static void Move()
        {
            if (left && map[y, x - 1] <= 0)
            {
                map[y, x] = 0;
                x--;
                map[y, x] = 3;
            }
            else if (right && map[y, x + 1] <= 0)
            {
                map[y, x] = 0;
                x++;
                map[y, x] = 3;
            }
        }
